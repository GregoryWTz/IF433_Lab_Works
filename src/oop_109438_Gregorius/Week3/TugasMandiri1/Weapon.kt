package oop_109438_Gregorius.Week3.TugasMandiri1

class Weapon(val name: String) {
    var damage: Int = 0
        set(value) {
            if (value < 0) {
                println("Peringatan: Damage tidak boleh negatif! Nilai tetap $field.")
            } else if (value > 1000) {
                println("Peringatan: Weapon Overpowered! Damage dibatasi ke 1000.")
                field = 1000
            } else {
                field = value
            }
        }

    val tier: String
        get() = when {
            damage > 800 -> "Legendary"
            damage > 500 -> "Epic"
            else -> "Common"
        }
}